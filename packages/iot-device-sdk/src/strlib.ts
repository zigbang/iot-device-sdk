/**
 * check if the string is a valid RFC 3339 date.
 * {@link https://tools.ietf.org/html/rfc3339}
 * options {@link IsISO8601Options}
 * 
 * @param args The string to validate
 * @returns string
 * @category Date Validator
 */

export function strconcat(...args: string[]): string {
    return args.join('');
}


